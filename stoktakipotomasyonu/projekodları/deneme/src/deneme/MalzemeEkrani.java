 public void dinamikAra(String ara){
        
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        
        
        malzeme_tablosu.setRowSorter(tr);
        
        
        tr.setRowFilter(RowFilter.regexFilter(ara));
        
        
    }
   public void malzemeGoruntule(){
        model.setRowCount(0);
         MalzemeIslemleri islem1=new MalzemeIslemleri(); 
        ArrayList<Malzeme> malzemeler = islem1.malzemeGetir();
        if(malzemeler != null){
            for(Malzeme malzeme: malzemeler){
                Object[] eklenecek={malzeme.getMalzeme_id(),malzeme.getMalzeme_ad(), malzeme.getMalzeme_kategori(), malzeme.getMalzeme_adet()};
                model.addRow(eklenecek);
                
            }
            }
        }
