    private void malzeme_guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_malzeme_guncelleActionPerformed
         // TODO add your handling code here:
         String malzeme_ad = malzeme_adi_alani.getText();
        String malzeme_kategori =  malzeme_kategori_adi_alani.getText();
        String malzeme_adet =  malzeme_adet_Alani.getText();
        
 int selectedrow = malzeme_tablosu.getSelectedRow();
        
        if (selectedrow == -1) {
            
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("Malzeme Tablosu şu anda boş. ");
            }
            else {
                mesaj_yazisi.setText("Lütfen güncellenecek bir malzeme bilgisi seçin.");
            }
        }
        else {
            
            
            
            
            int malzeme_id = (int)model.getValueAt(selectedrow,0);
            
            islemler.malzemeGuncelle(malzeme_id,malzeme_ad,malzeme_kategori,malzeme_adet);
            
            malzemeGoruntule();
            
            mesaj_yazisi.setText("Çalışan başarıyla güncellendi...");
        }
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_malzeme_guncelleActionPerformed

    private void malzeme_silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_malzeme_silActionPerformed
        // TODO add your handling code here:
         mesaj_yazisi.setText("");
       
       
       int selectedrow = malzeme_tablosu.getSelectedRow();
       
       if (selectedrow == -1) {
           if (model.getRowCount() == 0 ) {
               mesaj_yazisi.setText("Malzeme tablosu şu anda boş...");
           }
           else {
               mesaj_yazisi.setText("Lütfen silinecek bir malzeme bilgisi seçin...");
           }
      
       }
       else {
           int malzeme_id = (int)model.getValueAt(selectedrow,0);
           
           islemler.malzemeSil(malzeme_id);
           
       malzemeGoruntule();
           
           mesaj_yazisi.setText("Malzeme başarıyla silindi...");
           
        
       }  
    }//GEN-LAST:event_malzeme_silActionPerformed

